package id.lombokit.emarkethamzanwadi.Models;

public class Berita {
    private String image;
    private String judul;
    private String tgl_post;
    private String jam_post;
    private String isi_berita;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getTgl_post() {
        return tgl_post;
    }

    public void setTgl_post(String tgl_post) {
        this.tgl_post = tgl_post;
    }

    public String getJam_post() {
        return jam_post;
    }

    public void setJam_post(String jam_post) {
        this.jam_post = jam_post;
    }

    public String getIsi_berita() {
        return isi_berita;
    }

    public void setIsi_berita(String isi_berita) {
        this.isi_berita = isi_berita;
    }
}
